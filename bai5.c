#include <stdio.h>
int main () {
    int Sothu1 = 0, Sothu2 = 0;
    printf ("Nhap so thu nhat: ");
    scanf ("%d",&Sothu1);
    printf ("Nhap so thu hai: ");
    scanf ("%d",&Sothu2);
    printf ("Tich = %d\n", Sothu1 * Sothu2);
    printf ("Thuong = %d\n", Sothu1 / Sothu2);
    return 0;
}
